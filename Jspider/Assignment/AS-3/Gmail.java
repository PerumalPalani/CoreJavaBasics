class Gmail
{
	public static void main(String args[])
	{
		System.out.println("...Welcome to Gmail...");
		signup();
		login();
		homepage();
		logout();
		System.out.println("...Thank you for using Gmail....");
	}
	static void signup()
	{
		System.out.println("Signup the gmail");
	}
	static void login()
	{
		System.out.println("Login the Gmail account");
	}
	static void homepage()
	{
		System.out.println("This is the home page of Gmail");
	}
	static void logout()
	{
		System.out.println("Logout the gmail");
	}
}