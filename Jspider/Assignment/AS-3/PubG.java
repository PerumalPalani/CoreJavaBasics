class PubG
{
	static void signup()
	{
		System.out.println("PubG Signup");
	}
	static void login()
	{
		System.out.println("login the pubg with help of user_name and password");
	}
	static void home_page()
	{
		System.out.println("this is a home page");
	}
	static void order()
	{
		System.out.println("Order something");
	}
	public static void main(String args[])
	{
		System.out.println("...Welcome to Pubg...");
		signup();
		login();
		home_page();
		order();
		logout();
		System.out.println("..thank you for using Pubg..");
	}
	static void logout()
	{
		System.out.println("Logout this page");
	}
}

