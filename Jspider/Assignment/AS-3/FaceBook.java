class FaceBook
{
	static void signup()
	{
		System.out.println("Signup the facebook");
	}
	public static void main(String args[])
	{
		System.out.println("...Welcome to the Facebook..");
		signup();
		login();
		homePage();
		logout();
		System.out.println("...Thank you for using FaceBook..");
	}
	static void login()
	{
		System.out.println("Login the page of with the help of user_name & password");
	}
	static void homePage()
	{
		System.out.println("Homepage is displayed");
	}
	static void logout()
	{
		System.out.println("Logout the Facebook");
	}
}

